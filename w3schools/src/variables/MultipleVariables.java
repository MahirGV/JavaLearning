package variables;

public class MultipleVariables {

	public static void main(String[] args) {
		//bunu yazmaqdansa
	    int a = 4;
	    int b = 4;
	    int c = 4;
	    System.out.println(a + b + c);
	    
	    //bunu yazmaq daha effektli olar
	    int x = 5 , y = 4 , z = 3;
	    System.out.println(x + y + z);
	    
	    //eyni tipli coxluq 
	    int q, w, e ;
	    q = w = e = 31;
	    System.out.println(q + w + e);
	    
	    
	    
	}
}
